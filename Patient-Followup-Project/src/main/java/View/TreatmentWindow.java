    private void saveSideEffectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveSideEffectButtonActionPerformed
        windowTreatment.setSideeffect(sideEffectTextArea.getText());
                // Save changes
        try {
            treatmentCtrl.edit(windowTreatment);
            LOGGER.debug("Edited treatment (id = %d)".formatted(windowTreatment.getIdtreatment()));
        } catch (NonexistentEntityException | IllegalOrphanException ex) {
            LOGGER.error("Couldn't edit patient", ex);
        } catch (Exception ex){
            LOGGER.error("Couldn't edit patient", ex);
        }
        
        this.dispose();  
    }//GEN-LAST:event_saveSideEffectButtonActionPerformed

